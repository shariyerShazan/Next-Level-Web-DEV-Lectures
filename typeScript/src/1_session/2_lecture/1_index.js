const data = {
    name: "Shazan" ,
    "Age" : 22 ,
    adult : "Yes" ,
    true : "true"
}

console.log(data.name) // output -> Shazan
console.log(data["Age"]) // output -> 22
console.log(data["adult"]) // output -> Yes
console.log(data[true]) // output -> true
console.log(data.true) // output -> true